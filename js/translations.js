export function changeLanguage(newLang) {
  let languages = Object.keys(translations);
  if (languages.find(lang => lang === newLang)) {
    window.currentLanguage = newLang;
    translate();
  }
}

export function translate() {
  if (!window.currentLanguage) {
    let userLang = window.navigator.language.slice(0, 2);
    window.currentLanguage = translations[userLang] ? userLang : "en"
  }
  let matches = document.querySelectorAll("[translate]");
  matches.forEach((item) => {
    item.innerHTML = translations[window.currentLanguage][item.getAttribute('translate')];
  });
}

